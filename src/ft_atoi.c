#include "../includes/libft.h"

int		ft_atoi(const char *str)
{
	int num = 0;
	int sign = 1;

	while (*str <= 32 && * str != 127)
		str++;
	if (*str == '+' || *str == '-')
	{
		if (*str == '-')
			sign = -sign;
		str++;
	}
	while (*str >= '0' && *str <= '9')
	{
		num = num * 10 + *str++ - '0';
	}
	return (num * sign);
}
